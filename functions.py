def addition(number1,number2,number3):
    ans = number1 + number2 + number3
    return ans

def subtraction(number1,number2,number3):
    ans = number1 - number2 - number3
    return ans

def multiplication(number1,number2,number3):
    ans = number1 * number2 * number3
    return ans

def division(number1,number2,number3):
    ans = number1 / number2 / number3
    return ans

ans = addition(10,20,30)
print(ans)

ans = subtraction(10,20,30)
print(ans)

ans = multiplication(10,20,30)
print(ans)

ans = division(10,20,30)
print(ans)